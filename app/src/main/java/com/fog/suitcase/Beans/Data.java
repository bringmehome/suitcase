public class Data {
    public String device;
    public String createtime;
    public String lasttime;
    public String ip;
    public String port;
    public String longitude;
    public String latitude;
    public boolean latch_switch;
    public boolean case_lost;
    public String note;

    public String getDevice() {
        return device;
    }

    public void setDevice(String device) {
        this.device = device;
    }

    public String getCreatetime() {
        return createtime;
    }

    public void setCreatetime(String createtime) {
        this.createtime = createtime;
    }

    public String getLasttime() {
        return lasttime;
    }

    public void setLasttime(String lasttime) {
        this.lasttime = lasttime;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public boolean isLatch_switch() {
        return latch_switch;
    }

    public void setLatch_switch(boolean latch_switch) {
        this.latch_switch = latch_switch;
    }

    public boolean isCase_lost() {
        return case_lost;
    }

    public void setCase_lost(boolean case_lost) {
        this.case_lost = case_lost;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
